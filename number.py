# pembulatan angka dipaksa keatas
# ceil => keatas
import math

number = 5.4
number = math.ceil(number)

print(number)

# floor => ke bawah

number_ke_kebawah = 5.8
number_ke_kebawah = math.floor(number_ke_kebawah)
print(number_ke_kebawah)


